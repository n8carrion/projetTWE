<h1>À propos de La REZerve</h1>
<p>À la résidence Léonard de Vinci (résidence étudiante de Centrale Lille, connue sous le nom de
“REZ”), il est courant de voir apparaître, surtout lors des départs en stage ou à l’international, des
objets abandonnés ou déposés dans les couloirs : vêtements, vaisselle, petits meubles,
électroménager,EPI etc.
</p>

<p>Ces “dons sauvages”, bien que venant d’une bonne intention, posent problème : ils compliquent le
travail du personnel d’entretien. De plus, ils contreviennent au règlement de la résidence, qui interdit
le dépôt d’objets dans les couloirs</p>

<p>Pourtant, ces objets ont encore de la valeur et pourraient parfaitement servir à d'autres
résidents. Donner ou prêter un objet, c’est éviter un achat neuf, réduire le gaspillage et encourager une
logique de consommation plus responsable.
</p>

<p>
    C’est dans ce contexte que nous souhaitons créer La REZerve, une plateforme dédiée au don et au prêt d’objets entre étudiants et associations de Centrale Lille. Ce site permettra de centraliser les objets disponibles, de faciliter les échanges, et surtout, d’éviter les dépôts non autorisés. Grâce à une interface simple, chacun peut proposer des objets ou rechercher ce dont il a besoin, en quelques clics.
</p>

<p>Ce projet s’inscrit pleinement dans la démarche Développement Durable et Responsabilité
Sociétale (DDRS) portée par Centrale Lille Institut. Il répond notamment à deux Objectifs de
Développement Durable (ODD) de l’Agenda 2030 :</p>

